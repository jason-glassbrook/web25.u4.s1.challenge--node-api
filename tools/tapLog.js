/***********************************************************
  tapLog
***********************************************************/

const _ = require ('lodash/fp')
const clog = require ('./clog')

/***************************************
  MAIN
***************************************/

const tapLog = (message) =>
  _.tap ((x) => {
    _.cond ([
      [ _.isString, clog ],
    ]) (message)
    clog (x)
  })

/**************************************/

module.exports = tapLog
